while True:
  month = input('What number of the month is it? ')

  if month =='12' or month =='1' or month =='2':
    print('It is Winter')
    break
  elif month =='3' or month =='4' or month =='5':
    print('It is Spring')
    break
  elif month =='7' or month =='8' or month =='6':
    print('It is Sumer')
    break
  elif month =='10' or month =='11' or month =='9':
    print('It is Autumn')
    break
  else:
    print('Invalid')